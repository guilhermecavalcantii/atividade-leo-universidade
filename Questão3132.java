public class Questão3132 {
    public static void main (String[] args){
        // imprimir a soma de 1 até 1000
        int soma =  0;
        //cria um loop de 1 até 1000 
        for (int i = 1; i <= 1000; i++){
            soma += i;

        }

        System.out.println("A soma de 1 até 1000 é :" +soma);
    }
}